#include <stdio.h>

void matMul(int M1[2][2],int M2[2][2],int M3[2][2]){
    int p1 = M1[0][0]*(M2[0][1]-M2[1][1]);
    int p2 = (M1[0][0]+M1[0][1])*M2[1][1];
    int p3 = (M1[1][0]+M1[1][1])*M2[0][0];
    int p4 = M1[1][1]*(M2[1][0]-M2[0][0]);
    int p5 = (M1[0][0]+M1[1][1])*(M2[0][0]+M2[1][1]);
    int p6 = (M1[0][1]-M1[1][1])*(M2[1][0]+M2[1][1]);
    int p7 = (M1[0][0]-M1[1][0])*(M2[0][0]+M2[0][1]);


    M3[0][0] = p5+p4-p2+p6;
    M3[0][1] = p1+p2;
    M3[1][0] = p3+p4;
    M3[1][1] = p1+p5-p3-p7;
}

int main(){
    int M1[2][2] = {{2,4},{7,3}};
    int M2[2][2] = {{1,4},{2,3}};
    int M3[2][2];
    matMul(M1, M2, M3);
    for (int i = 0; i<2; i++){
        for (int j = 0; j<2; j++){
            printf("%d ",M3[i][j]);
        }
        printf("\n");
    }
}